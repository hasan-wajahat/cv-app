/**
 * shorthand for min-width media query
 * @param {number} width in pixels
 */
export const minWidthQuery = width => `@media (min-width: ${width}px)`;

/**
 * shorthand for max-width media query
 * @param {number} width in pixels
 */
export const maxWidthQuery = width => `@media (max-width: ${width}px)`;
